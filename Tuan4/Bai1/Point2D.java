public class Point2D{
    private float x=0.0f, y=0.0f;

    public Point2D(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public Point2D(){
        
    }

    public float getX() {
        return this.x;
    }
    public float getY() {
        return this.y;
    }

    

}